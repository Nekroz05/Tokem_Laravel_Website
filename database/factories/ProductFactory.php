     * The name of the factory's corresponding model.
     *
     * @var string
     */
    protected $model = Product::class;

    /**
     * Define the model's default state.
     *
     * @return array
     */
    public function definition()
    {
        return [
            'cart_id' => rand(1, 5),
            'product_detail_id'=>rand(1,9),
            'quantity' => rand(1,10),
        ];
    }
}
